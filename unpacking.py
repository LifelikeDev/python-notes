# Storing elements in a list / tuple in variables for easy accessibility

numbers = (2, 3, 4, 5)
a = numbers[0]
b = numbers[1]
c = numbers[2]
d = numbers[3]
print(a)
print(b)
print(c)
print(d)
print()

# Unpacking makes the above easier

j, k, l, m = numbers
print(j)
print(k)
print(l)
print(m)
print()

# In lists,
milestones = [200, 400, 800, 1200, 1600]

i, ii, iii, iv, v = milestones
print(i)
print(ii)
print(iii)
print(iv)
print(v)